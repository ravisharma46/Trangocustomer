package com.naruto.trango.homepage.SimpleClasses;

public class Container5 {

    private String numbers;
    private String shorDes;
    private String desc;



    public Container5(String numbers, String shorDes, String desc) {
        this.numbers = numbers;
        this.shorDes = shorDes;
        this.desc = desc;
    }

    public String getNumbers() {
        return numbers;
    }

    public void setNumbers(String numbers) {
        this.numbers = numbers;
    }

    public String getShorDes() {
        return shorDes;
    }

    public void setShorDes(String shorDes) {
        this.shorDes = shorDes;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
}
